#include <stdio.h>


int main()
{
    int max_unhappy_day = 0;
    int max_time = -1;
    int today_time;
    int i;
    for (i = 1; i < 8; ++i)
    {
        int a, b;
        scanf("%d %d", &a, &b);
        today_time = a + b;
        if (today_time > 8)
        {
            if (today_time > max_time)
            {
                max_time = today_time;
                max_unhappy_day = i;
            }
        }
    }
    printf("%d\n", max_unhappy_day);
}

